<li>The <strong>broker</strong> that arranged the load.</li>
<li>The <strong>cargo loader</strong> if shifting or overweight cargo contributed.</li>
<li>The <strong>truck or parts manufacturer</strong> if a mechanical failure was involved.</li>
</ul>
