<h2>When can my child ride in a booster seat in South Carolina?</h2>
<p>A child moves into a booster seat once they have outgrown the height or weight limits of their forward-facing harness seat. The booster&rsquo;s job is to raise the child so the vehicle&rsquo;s adult lap-and-shoulder belt crosses the strong parts of the body &mdash; the lap belt low across the hips and the shoulder belt across the center of the shoulder and chest, not the neck or abdomen. Safety experts widely recommend keeping a child in a booster until the seat belt fits correctly without it, which often means staying in a booster longer than the bare legal minimum. Under South Carolina law, a child generally moves into a booster from <strong>age 4</strong> (after outgrowing the forward-facing harness) and stays in it until they are at least 8 years old or 57 inches tall and the belt fits properly.</p>

<h2>When can my child stop using a booster and use a seat belt?</h2>
<p>A child is ready for an adult seat belt alone when the belt fits properly: the child can sit all the way back against the seat, knees bend comfortably at the edge, the lap belt sits low on the hips (not the stomach), and the shoulder belt crosses the chest and shoulder rather than the neck or face. Because children grow at different rates, fit &mdash; not just age &mdash; is what matters. Moving a child out of a booster too early is one of the most common car-seat mistakes and increases injury risk in a crash. Under S.C. Code &sect; 56-5-6410, a child may use the adult belt alone once they are <strong>at least 8 years old or at least 57 inches tall</strong> and the belt passes a three-part fit test: the lap belt crosses the thighs and hips (not the abdomen), the shoulder belt crosses the center of the chest (not the neck), and the child can sit back against the seat with knees bent over the edge.</p>

<h2>What is the safest practice, beyond the legal minimum?</h2>
<p>The law sets a floor, not a ceiling. According to child-passenger-safety guidance from the National Highway Traffic Safety Administration, children are safest when they remain in each stage of restraint &mdash; rear-facing, forward-facing harness, and booster &mdash; until they reach the upper height or weight limit of that seat, rather than graduating to the next stage at the earliest allowed moment. Practically, that means:</p>
<ul>
<li>Keep your child <strong>rear-facing</strong> as long as the seat allows.</li>
<li>Use the <strong>harness</strong> until the child outgrows it by height or weight.</li>
<li>Keep the child in a <strong>booster</strong> until the adult belt fits correctly.</li>
<li>Children under 13 are safest in the <strong>back seat</strong>.</li>
</ul>

<h2>How do car seat laws affect an injury claim in South Carolina?</h2>
