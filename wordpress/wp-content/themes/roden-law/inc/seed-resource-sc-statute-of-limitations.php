<h2>When does the clock start? The discovery rule</h2>
<p>The clock usually starts on the date you were injured. But South Carolina recognizes a <strong>discovery rule</strong>: for certain injuries that are not immediately obvious, the 3-year period can begin when you knew, or reasonably should have known, that you were harmed and that someone else&rsquo;s conduct may have caused it. This matters most in cases like medical malpractice or toxic exposure, where the harm can surface long after the underlying negligence.</p>
<p>The discovery rule is fact-specific and frequently disputed by insurers, who argue the clock started earlier to get your case thrown out. Do not assume it extends your deadline &mdash; have an attorney evaluate exactly when your clock started.</p>

<h2>What is the deadline for medical malpractice in South Carolina?</h2>
<p>Medical malpractice has its own timing rules under <strong>S.C. Code &sect; 15-3-545</strong>. In general, a South Carolina medical malpractice claim must be filed within <strong>3 years of the date you discovered (or reasonably should have discovered) the injury</strong>, but no later than an outside <strong>6-year statute of repose</strong> measured from the date of the negligent act &mdash; a hard cap that can bar a claim even if the harm was not yet discovered. A narrow exception applies when a foreign object is left in the body: that claim runs <strong>2 years from discovery</strong> (and in no event less than 3 years from when the object was left). South Carolina also requires a pre-suit <strong>Notice of Intent to File Suit</strong> and an expert affidavit under S.C. Code &sect; 15-79-125 before a malpractice case can move forward.</p>
<p>Because the discovery and repose rules interact, malpractice deadlines are among the easiest to miscalculate. If you suspect a medical error, have the timeline reviewed promptly.</p>

<h2>What if my claim is against a city, county, or the state?</h2>
<p>Claims against government entities &mdash; a municipal vehicle, a public hospital, a state or county agency, a public road defect &mdash; fall under the <strong>South Carolina Tort Claims Act</strong> (S.C. Code &sect; 15-78-10 and following). These claims carry a <strong>shorter, 2-year deadline</strong> to file suit, rather than the usual 3 years. You can extend that to <strong>3 years</strong> by first filing a "verified claim" with the responsible agency. Filing a verified claim is optional &mdash; South Carolina does not require one before you sue &mdash; but doing so (within 1 year of the loss) both buys the extra year and starts the settlement process. Because the default deadline is only 2 years and government claims follow their own procedures, treat any claim against a public entity as time-sensitive.</p>
<p>The practical takeaway is simple: if any government body may be responsible for your injury, assume the deadline is short and contact an attorney right away. Waiting can forfeit an otherwise strong claim.</p>

<h2>Are there exceptions that pause or extend the deadline?</h2>
<p>South Carolina recognizes a handful of situations that can pause ("toll") or shift the clock:</p>
<ul>
<li><strong>Injured minors.</strong> When the injured person is a child, the statute of limitations is generally tolled &mdash; the clock does not run the same way it would for an adult. The specific tolling rules depend on the child&rsquo;s age and the type of claim, so have a lawyer confirm the deadline for any claim involving a minor.</li>
<li><strong>The discovery rule.</strong> As above, certain non-obvious injuries start the clock when the harm is or should have been discovered.</li>
<li><strong>Wrongful death.</strong> A South Carolina wrongful death claim must be brought by the estate&rsquo;s <strong>personal representative</strong> (S.C. Code &sect; 15-51-20), and timing runs from the date of death.</li>
</ul>
<p>Exceptions are narrow and heavily litigated. Never assume one applies to you without legal advice &mdash; the safest course is always to act as if the standard deadline is running.</p>

<h2>What happens if I miss the deadline?</h2>
<p>If you file after the statute of limitations has expired, the defendant will move to dismiss, and South Carolina courts routinely grant that motion. It does not matter how serious your injuries are or how clearly the other party was at fault &mdash; a late claim is almost always barred, and you recover nothing. The deadline is the single most unforgiving rule in injury law, which is exactly why it should never be left to the last minute.</p>
