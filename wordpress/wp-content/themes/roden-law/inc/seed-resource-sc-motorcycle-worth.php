<h2>Why a Drunk-Driver Motorcycle Crash May Be Worth More in South Carolina</h2>
<p>A South Carolina motorcycle case can be worth substantially more when the at-fault driver was impaired, because the usual limit on punitive damages can be removed entirely. Punitive damages are meant to punish and deter especially dangerous conduct, and they are awarded on top of your compensatory damages.</p>
<p>Under <strong>S.C. Code § 15-32-530</strong>, punitive damages are generally capped at the greater of three times compensatory damages or a statutory minimum that is <strong>adjusted annually for inflation (roughly $739,000 as of 2026)</strong>. But under <strong>S.C. Code § 15-32-530(C)</strong>, that cap is <strong>removed entirely</strong> when the defendant was <strong>driving under the influence of alcohol or drugs</strong>, is convicted of a felony arising from the conduct, or acted with intent to harm. Drunk-driving motorcycle crashes are one of the clearest situations where this exception applies, and it can meaningfully change what a serious claim is ultimately worth.</p>
<p>Graeham C. Gillin, a partner at Roden Law\'s Charleston office, notes that documenting a driver\'s impairment early — through the crash report, toxicology, and any DUI charge — is one of the most important steps in a South Carolina motorcycle case, because it can open the door to uncapped punitive damages.</p>

<h2>How South Carolina Law Affects Your Payout</h2>
<p>South Carolina law affects your payout in three major ways: the deadline to file, how your own fault reduces your award, and the availability of punitive damages. Each can raise or lower the final number.</p>

<h3>The filing deadline (statute of limitations)</h3>
<p>In South Carolina, you generally have <strong>3 years</strong> from the date of the crash to file a motorcycle accident lawsuit under <strong>S.C. Code § 15-3-530</strong>. Miss that deadline and you typically lose the right to recover anything, which is why acting early to preserve evidence protects your case value.</p>

<h3>Comparative fault reduces your award</h3>
<p>South Carolina follows <strong>modified comparative negligence</strong>: you can recover as long as you are <strong>less than 51% at fault</strong>, but your award is reduced by your percentage of fault. If your case is worth $300,000 and you are found 20% at fault, you recover $240,000. At 51% or more fault, you recover nothing. Insurers often try to blame the rider — for speed, lane position, or not wearing a helmet — so how fault is assigned has a direct dollar impact. Learn how <a href="/resources/south-carolina-comparative-negligence/">South Carolina comparative negligence</a> works.</p>

<h3>No cap on compensatory damages</h3>
<p>Unlike medical-malpractice claims, your South Carolina motorcycle case has <strong>no cap on pain-and-suffering or any other compensatory damages</strong>. Your recovery is limited by the facts and the available insurance, not by a statutory ceiling — which matters enormously in catastrophic cases.</p>

<h2>How Motorcycle Accident Settlements Are Calculated</h2>
<p>A motorcycle accident settlement is calculated by adding your economic damages to your non-economic damages, then adjusting for fault and available insurance.</p>
<ul>
<li><strong>Special damages</strong> (economic) are added up from records — medical bills, wage statements, and repair estimates produce a hard number.</li>
<li><strong>General damages</strong> (non-economic) are harder to quantify. A common informal method is the "multiplier," where pain and suffering is estimated as economic damages multiplied by a figure (often roughly 1.5 to 5) based on injury severity and permanence.</li>
<li><strong>Fault and coverage</strong> then adjust the total — reduced by your comparative-fault share and practically limited by how much insurance the defendants carry, including any stacked UM/UIM coverage.</li>
</ul>
<p>The multiplier is a rough industry concept, not a South Carolina legal formula. No statute sets a multiplier, and no lawyer can promise a particular number. The real value of your case comes from its specific facts and evidence.</p>

